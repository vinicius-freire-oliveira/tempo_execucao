# Importando a biblioteca 'time', que permite medir o tempo de execução
import time

# Registrando o tempo de início da execução
start = time.time()

# Imprimindo uma mensagem qualquer
print("O tempo usado para executar isto é dado abaixo")

# Registrando o tempo de término da execução
end = time.time()

# Calculando e imprimindo o tempo de execução em segundos
print(end - start)
